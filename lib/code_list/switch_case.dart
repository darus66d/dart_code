void main(){

  //Example 1: basic switch  case

  int day = 3;

  print("\nExample 1: Basic Switch Case");

  switch(day){
    case 1:
      print("Sunday");
      break;
    case 2:
      print("Monday");
      break;
    case 3:
      print("Tuesday");
      break;
    case 4:
      print("wednesday");
      break;
    case 5:
      print("Thursday");
      break;
    case 6:
      print("Friday");
      break;
    default:
      print("Saturday");

  }
}